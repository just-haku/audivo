export function safeSetText(id, text) {
    const el = document.getElementById(id);
    if (el) el.textContent = text;
}

export function safeSetPlaceholder(id, text) {
    const el = document.getElementById(id);
    if (el) el.placeholder = text;
}

// Added translation helper
export function t(key, params = {}) {
    const lang = localStorage.getItem('turboVideoLang') || 'en-US';
    let text = TRANSLATIONS[lang]?.[key] || TRANSLATIONS['en-US']?.[key] || key;
    for (const [k, v] of Object.entries(params)) {
        text = text.replace(new RegExp(`{${k}}`, 'g'), v);
    }
    return text;
}
export function getActiveLanguage() {
    return localStorage.getItem('turboVideoLang') || 'en-US';
}
